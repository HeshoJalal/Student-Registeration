    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
         String sqlPre="DELETE FROM course WHERE id=?";
        try {
            PreparedStatement pst=con.c.prepareStatement(sqlPre);
            pst.setString(1, returnCell());
            pst.execute();
            reloadTable();

           JOptionPane.showMessageDialog(this, "Course deleted Successfully","SuccessFul",JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(InsertStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
         try {
        
            if (title.getText().isEmpty()) {
                titleError.setText("Please enter a course title!!");
                titleError.setVisible(true);
            } 
            
            else{
           String sqlPre="UPDATE course SET title=? WHERE ID=?";
           PreparedStatement pst=con.c.prepareStatement(sqlPre);
            pst.setString(1, title.getText());
            pst.setString(2, returnCell());

           pst.executeUpdate();
           reloadTable();
           JOptionPane.showMessageDialog(this, "Course updated Successfully","SuccessFul",JOptionPane.INFORMATION_MESSAGE);
                     
            }
            
        } catch (Exception e) {
        }    
    }//GEN-LAST:event_updateActionPerformed

