    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
 try {
        
            if (title.getText().isEmpty()) {
                titleError.setText("Please enter a course title!!");
                titleError.setVisible(true);
            } 
            
            else{
           String sqlPre="INSERT INTO course(title) VALUES(?)";
           PreparedStatement pst=con.c.prepareStatement(sqlPre);
           pst.setString(1, title.getText().toUpperCase());
           pst.executeUpdate();
           reloadTable();
           JOptionPane.showMessageDialog(this, "Course added Successfully","SuccessFul",JOptionPane.INFORMATION_MESSAGE);
                     
            }
            
        } catch (Exception e) {
        }    
    }//GEN-LAST:event_addActionPerformed

